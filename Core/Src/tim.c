void Set_PWM(int PWM1, int PWM2)
{
  if(PWM1 > 0)
  {
    HAL_GPIO_WritePin(AIN1_GPIO_Port, AIN1_Pin, GPIO_PIN_SET);		// 高电平	AIN1	1
    HAL_GPIO_WritePin(AIN2_GPIO_Port, AIN2_Pin, GPIO_PIN_RESET);	// 低电平	AIN2	0
  }
  else
  {
    HAL_GPIO_WritePin(AIN2_GPIO_Port, AIN2_Pin, GPIO_PIN_SET);		// 高电平	AIN2	1   
    HAL_GPIO_WritePin(AIN1_GPIO_Port, AIN1_Pin, GPIO_PIN_RESET);	// 低电平	AIN1	0
    PWM1 = -PWM1;
  }
  if(PWM2 > 0)
  {
    HAL_GPIO_WritePin(BIN1_GPIO_Port, BIN1_Pin, GPIO_PIN_SET);		// 高电平	BIN1	1
    HAL_GPIO_WritePin(BIN2_GPIO_Port, BIN2_Pin, GPIO_PIN_RESET);	// 低电平	BIN2	0
  }
  else
  {
    HAL_GPIO_WritePin(BIN1_GPIO_Port, BIN1_Pin, GPIO_PIN_RESET);	// 低电平	BIN1	0
    HAL_GPIO_WritePin(BIN2_GPIO_Port, BIN2_Pin, GPIO_PIN_SET);		// 高电平	BIN2	1
    PWM2 = -PWM2;
  }
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, PWM1);
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, PWM2);
}

